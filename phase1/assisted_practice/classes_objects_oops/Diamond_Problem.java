package phase1.assisted_practice.classes_objects_oops;

interface First 
{  
    default void show() 
    { 
        System.out.println("Default First"); 
    } 
} 
interface Second 
{  
    default void show() 
    { 
        System.out.println("Default Second"); 
    } 
}  
public class Diamond_Problem implements First, Second 
{  
    public void show() 
    {  
        First.super.show(); 
        Second.super.show(); 
    } 
    public static void main(String args[]) 
    { 
    	Diamond_Problem ob = new Diamond_Problem(); 
        ob.show(); 
    } 
}
